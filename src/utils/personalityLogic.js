// 4. 통합 성격 풀이 생성
export const getPersonalityAnalysis = (sajuData) => {
  const dayStemChar = sajuData.dayPillarHanja?.[0];
  const monthBranchChar = sajuData.monthPillarHanja?.[1];

  if (!dayStemChar || !monthBranchChar) return null;

  const stemInfo = dayStemPersonality[dayStemChar];
  const branchInfo = monthBranchPersonality[monthBranchChar];
  const johu = getJohu(dayStemChar, monthBranchChar);

  if (!stemInfo || !branchInfo) return null;

  return {
    dayStem: dayStemChar,
    monthBranch: monthBranchChar,
    core: stemInfo.core,
    keyword: stemInfo.keyword,
    traits: stemInfo.traits,
    shadow: stemInfo.shadow,
    social: branchInfo.social,
    season: branchInfo.season,
    mood: branchInfo.mood,
    johu: johu
  };
};
