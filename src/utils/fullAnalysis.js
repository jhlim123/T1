export const getFullAnalysis = (sajuData) => {
  const ds = sajuData.dayPillarHanja?.[0];
  const db = sajuData.dayPillarHanja?.[1];
  const mb = sajuData.monthPillarHanja?.[1];
  if (!ds || !db) return null;

  // 1. 일주 기질
  const dayPillar = ds + db;
  const dayPillarInfo = dayPillarDesc[dayPillar] || `${dayPillar} — 일간과 일지의 기질이 독특하게 조합된 명식입니다.`;

  // 2. 오행 분포
  const allChars = [
    sajuData.yearPillarHanja?.[0], sajuData.yearPillarHanja?.[1],
    sajuData.monthPillarHanja?.[0], mb,
    ds, db,
    sajuData.hourPillarHanja?.[0], sajuData.hourPillarHanja?.[1]
  ].filter(Boolean);

  const elementDist = { '木':0,'火':0,'土':0,'金':0,'水':0 };
  allChars.forEach(c => {
    const idx = elementMap[c];
    if (idx !== undefined) elementDist[elementNames[idx]]++;
  });

  const sorted = Object.entries(elementDist).sort((a, b) => b[1] - a[1]);
  const excess = sorted.filter(([,v]) => v >= 3).map(([k]) => k);
  const lacking = sorted.filter(([,v]) => v === 0).map(([k]) => k);
  const mostLacking = lacking.length > 0 ? lacking : [sorted[sorted.length - 1][0]];

  // 3. 십성 분포
  const targets = [
    sajuData.yearPillarHanja?.[0], sajuData.yearPillarHanja?.[1],
    sajuData.monthPillarHanja?.[0], mb,
    db,
    sajuData.hourPillarHanja?.[0], sajuData.hourPillarHanja?.[1]
  ].filter(Boolean);

  const tenGodDist = {};
  targets.forEach(c => {
    const g = getTenGods(ds, c);
    if (g) tenGodDist[g] = (tenGodDist[g] || 0) + 1;
  });

  const sortedGods = Object.entries(tenGodDist).sort((a, b) => b[1] - a[1]);
  const dominantGod = sortedGods[0]?.[0] || '';
  const lifeInfo = tenGodLifeMap[dominantGod] || {};

  return {
    dayPillar, dayPillarInfo,
    elementDist, excess, lacking: mostLacking,
    tenGodDist, sortedGods, dominantGod, lifeInfo,
    remedies: mostLacking.map(el => ({ el, ...(elementRemedy[el] || {}) })),
    excessWarning, lackingAdvice
  };
};
